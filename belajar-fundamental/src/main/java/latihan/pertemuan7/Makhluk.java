public class Makhluk {

    private Integer jmlKaki;
    private Integer jmlGigi;
    private Boolean berbulu;

    public Integer getJmlKaki() {
        return jmlKaki;
    }

    public void setJmlKaki(Integer jmlKaki) {
        this.jmlKaki = jmlKaki;
    }

    public Integer getJmlGigi() {
        return jmlGigi;
    }

    public void setJmlGigi(Integer jmlGigi) {
        this.jmlGigi = jmlGigi;
    }

    public Boolean getBerbulu() {
        return berbulu;
    }

    public void setBerbulu(Boolean berbulu) {
        this.berbulu = berbulu;
    }
    
    
    
}
